#include <stdio.h>
#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	src_length;

	src_length = ft_strlen(src);
	if (size == 0)
		return (src_length);
	while (*src != '\0' && --size > 0)
		*dst++ = *src++;
	*dst = '\0';
	return (src_length);
}
